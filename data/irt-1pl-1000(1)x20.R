dataRaw <- fromJSON("data/irt-1pl-1000(1)x20.json")

dataRaw.responses <- c(1:(length(dataRaw) * length(dataRaw[[1]]$correct)))
dataRaw.subjectIds <- c(1:length(dataRaw.responses))
dataRaw.itemIds <- c(1:length(dataRaw.responses))

for (i in 1:length(dataRaw)) {
  for (j in 1:length(dataRaw[[i]][[1]])) {
    index <- (i - 1) * length(dataRaw[[i]][[1]]) + j
    
    dataRaw.responses[[index]] <- dataRaw[[i]]$correct[j]
    dataRaw.itemIds[[index]] <- dataRaw[[i]]$itemId[j] + 1
    dataRaw.subjectIds[[index]] <- dataRaw[[i]]$respondentId[j] + 1
  }
}

dataRaw.difficulties <- c(1:length(unique(dataRaw.itemIds)))
dataRaw.abilities <- c(1:length(unique(dataRaw.subjectIds)))

for (i in 1:length(dataRaw)) {
  for (j in 1:length(dataRaw[[i]][[1]])) {
    dataRaw.difficulties[dataRaw[[i]]$itemId[j] + 1] <- dataRaw[[i]]$beta[j]
    dataRaw.abilities[dataRaw[[i]]$respondentId[j] + 1] <- dataRaw[[i]]$thetas[[j]][1]
  }
}
